var langs = ['en', 'es'];
var current_lang_index = 0;
var current_lang = langs[current_lang_index];
var varTranslator = true;

window.change_lang = function() {
    current_lang_index = ++current_lang_index % 2;
    current_lang = langs[current_lang_index];
    translate();
}

function translate() {
    var languageButton = document.getElementById('language-button');
    var themeButton = document.getElementById('theme-button');
    var inputSubmit = document.getElementById('input-submit');

    varTranslator?languageButton.title = "Translate to spanish":languageButton.title = "Traducir a inglés";
    varTranslator?themeButton.title = "Change theme":themeButton.title = "Cambiar de tema";
    varTranslator?inputSubmit.value = "Send":inputSubmit.value = "Enviar";
    varTranslator=!varTranslator;

    $("[data-translate]").each(function(){
        var key = $(this).data('translate');
        $(this).html(dictionary[key][current_lang] || "N/A");
    });
}

translate();